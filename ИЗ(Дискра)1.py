"""Граф задан матрицей смежности. Определить, связный ли этот граф. Если да,
найти его центр, радиус и диаметр. Если нет - найти центр, радиус и диаметр той компоненты связности,
которая содержит больше всего вершин. Если несколько компонент связности содержит максимальное число
вершин, взять ту, которая содержит вершину с меньшим номером.
Указание: Для определения числа компонент связности использовать обход в ширину или глубину. Для
определения центра, радиуса и диаметра определить расстояния между всеми парами вершин с помощью
алгоритма Флойда."""
import numpy as np

# Алгоритм Флойда-Уоршелла для нахождения кратчайших путей
def floyd_warshall(graph):
    n = len(graph)
    dist = np.copy(graph)
    
    # Заменяем 0 (кроме диагонали) на большое значение (условное "бесконечное")
    for i in range(n):
        for j in range(n):
            if i != j and dist[i][j] == 0:
                dist[i][j] = float('inf')
    
    # Флойд-Уоршелл
    for k in range(n):
        for i in range(n):
            for j in range(n):
                if dist[i][k] != float('inf') and dist[k][j] != float('inf'):
                    dist[i][j] = min(dist[i][j], dist[i][k] + dist[k][j])
    
    return dist

# Поиск в глубину для нахождения компоненты связности
def dfs(graph, start, visited, component):
    visited[start] = True
    component.append(start)
    for neighbor, connected in enumerate(graph[start]):
        if connected and not visited[neighbor]:
            dfs(graph, neighbor, visited, component)
            
# Находит компоненты связности графа.
def find_connected_components(graph):
    n = len(graph)
    visited = [False] * n
    components = []

    for start in range(n):
        if not visited[start]:
            component = []
            dfs(graph, start, visited, component)
            components.append(component)
    
    return components

def analyze_component(graph, component):
    """
    Анализирует радиус, диаметр и центр компоненты графа.
    """
    # Создаем подграф для компоненты
    subgraph = [[graph[u][v] for v in component] for u in component]
    dist = floyd_warshall(subgraph)
    
    # Вычисляем радиус, диаметр и центр
    eccentricities = [max(filter(lambda x: x != float('inf'), row)) for row in dist]
    radius = min(eccentricities)
    diameter = max(eccentricities)
    center = component[eccentricities.index(radius)]
    
    return radius, diameter, center

def analyze_graph(graph):
    """
    Анализирует связность графа, определяет центр, радиус и диаметр.
    """
    components = find_connected_components(graph)
    
    if len(components) == 1:
        # Граф связный
        radius, diameter, center = analyze_component(graph, components[0])
        return f"Граф связан.\nРадиус: {radius}\nДиаметр: {diameter}\nЦентр: вершина {center}"
    else:
        # Граф несвязный: ищем самую крупную компоненту
        largest_component = max(components, key=lambda x: (len(x), -min(x)))
        radius, diameter, center = analyze_component(graph, largest_component)
        return (f"Граф несвязный.\nАнализ крупнейшей компоненты {largest_component}:\n"
                f"Радиус: {radius}\nДиаметр: {diameter}\nЦентр: вершина {center}")


