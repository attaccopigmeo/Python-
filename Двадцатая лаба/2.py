"""
Реализовать в виде рекурсивной процедуры или функции, itertools не использовать.
Последовательности длины n из чисел от 1 до k. Написать рекурсивную программу перечисления всех последовательностей длины n из чисел от 1 до k.
Формат входных данных: Входные данные содержат одну строку, в которой находится два целых числа сначала n (0<n<12), потом k (0<k<5).
Формат выходных данных: На выходе нужно получить все возможные последовательности из n чисел, где каждое число в пределах от 1 до k.\
    Каждая последовательность должна быть выведена в отдельной строке. Числа в последовательность не нужно разделять пробелами.\
        Последовательности нужно выводить в лексикографическом порядке. Результат выводить в текстовый файл
"""
def generate_sequences(n, k, current, result):
    if len(current) == n:
        result.append(''.join(map(str, current)))
        return
    for i in range(1, k + 1):
        generate_sequences(n, k, current + [i], result)

def main():
    input_data = input("Введите n и k через пробел: ")
    n, k = map(int, input_data.strip().split())

    result = []
    generate_sequences(n, k, [], result)

    result.sort()  # Лексикографическая сортировка (на всякий случай)


    with open("output.txt", "w") as f:
        for sequence in result:
            f.write(sequence + '\n')

    print("Результат записан в файл output.txt")

if __name__ == "__main__":
    main()
